const express = require("express");
const app = express();
const mysql = require("mysql");
const cors = require("cors");

app.use(cors());
app.use(express.json());

const db = mysql.createConnection({
  user: "root",
  host: "localhost",
  password: "1234",
  database: "baochay",
});

app.post("/create", (req, res) => {
  const device = req.body.device;
  const temperature = req.body.temperature;
  const humidity = req.body.humidity;


  db.query(
    "INSERT INTO nhietdo (device,temperature, humidity) VALUES (?,?,?)",
    [device,temperature, humidity],
    (err, result) => {
      if (err) {
        console.log(err);
      } else {
        res.send("Values Inserted");
      }
    }
  );
});

app.get("/nhietdo", (req, res) => {
  db.query("SELECT * FROM nhietdo", (err, result) => {
    if (err) {
      console.log(err);
    } else {
      res.send(result);
    }
  });
});

app.put("/update", (req, res) => {
  const id = req.body.id;
  const temperature = req.body.temperature;
  db.query(
    "UPDATE nhietdo SET temperature = ? WHERE id = ?",
    [temperature, id],
    (err, result) => {
      if (err) {
        console.log(err);
      } else {
        res.send(result);
      }
    }
  );
});

app.delete("/delete/:id", (req, res) => {
  const id = req.params.id;
  db.query("DELETE FROM nhietdo WHERE id = ?", id, (err, result) => {
    if (err) {
      console.log(err);
    } else {
      res.send(result);
    }
  });
});

app.listen(3001, () => {
  console.log("Yey, your server is running on port 3001");
});
